function registerDesktopMainIpc(deps) {
  const {
    app,
    BrowserWindow,
    Notification,
    autoUpdater,
    buildTrayTooltip,
    clearDesktopCaches,
    context,
    copyStickerIntoLibrary,
    createChatWindow,
    createListWindow,
    createLoginWindow,
    deleteStickerEntry,
    desktopApp,
    desktopUpdates,
    dialog,
    ensureRemoteAvatarCached,
    ensureStorageDirectories,
    fs,
    getDesktopPreferences,
    getStorageInfo,
    listStickerEntries,
    moveStickerEntry,
    path,
    readStateCache,
    registerDesktopIpcHandlers,
    renameStickerEntry,
    resolveTrayIconPath,
    resolveWindowByEvent,
    saveDownloadedAsset,
    setWindowContext,
    shell,
    showDesktopNotification,
    slideOutListWindow,
    shouldSuppressDesktopNotification,
    stickerDeps,
    toggleWindowMaximize,
    walkStickerFiles,
    windowHelpers,
    writeImageToClipboard,
    writeStateCache,
    trayDeps,
    focusWindow,
  } = deps;

  registerDesktopIpcHandlers({
    STICKER_EXTENSIONS: stickerDeps.STICKER_EXTENSIONS,
    app,
    autoUpdater,
    buildTrayMenu: desktopApp.buildTrayMenuForApp,
    buildTrayTooltip,
    clearDesktopCaches,
    copyStickerIntoLibrary,
    createChatWindow,
    createListWindow,
    createLoginWindow,
    deleteStickerEntry,
    dialog,
    ensureRemoteAvatarCached,
    ensureStorageDirectories,
    getDesktopPreferences,
    getStorageInfo,
    listStickerEntries,
    logout: desktopApp.logout,
    moveStickerEntry,
    markAppQuitting: desktopApp.markAppQuitting,
    pathApi: path,
    publishUpdateState: desktopUpdates.publishUpdateState,
    readStateCache,
    registerWindowContext: (window, nextContext) => setWindowContext(context.windows.windowContextById, window, nextContext),
    renameStickerEntry,
    resolveWindowByEvent: (event) => resolveWindowByEvent(BrowserWindow, event),
    saveDownloadedAsset,
    screenshotSelection: context.screenshotSelection,
    setUnreadCount: desktopApp.setUnreadCount,
    shell,
    showDesktopNotification: (payload) => showDesktopNotification({
      Notification,
      ...payload,
      getDesktopPreferences,
      resolveTrayIconPath: () => resolveTrayIconPath({ fs, path, process, projectRoot: context.projectRoot }),
      shouldSuppressDesktopNotification: (conversationId) => shouldSuppressDesktopNotification(BrowserWindow, context.windows.windowContextById, conversationId),
      createListWindow,
      createChatWindow,
      broadcastOpenConversation: (conversationId) => trayDeps.broadcastOpenConversation(conversationId, {
        listWindow: context.windows.listWindow,
        chatWindows: context.windows.chatWindows,
      }),
      slideOutListWindow,
      focusWindow,
      listWindow: () => context.windows.listWindow,
      showPrimaryWindowFromTray: desktopApp.openPrimaryFromTray,
    }),
    slideOutListWindow,
    targetOrigin: context.targetOrigin,
    toggleWindowMaximize,
    trayRef: () => context.windows.tray,
    updateDesktopPreferences: desktopApp.updateDesktopPreferences,
    updateStateRef: desktopUpdates.updateStateRef,
    walkStickerFiles,
    windowStateBuilder: windowHelpers.buildWindowState,
    writeImageToClipboard,
    writeStateCache,
  });
}

module.exports = {
  registerDesktopMainIpc,
};
